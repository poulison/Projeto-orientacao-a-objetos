public class Moedas {
    private double saldo;
    private double cotas;

    public double getSaldo() {
        return saldo;
    }

    public void setSaldo(double saldo) {
        this.saldo = saldo;
    }

    public double getCotas() {
        return cotas;
    }

    public void setCotas(double cotas) {
        this.cotas = cotas;
    }

    public Moedas() {
    }

    public Moedas(double saldo, double cotas) {
        this.saldo = saldo;
        this.cotas = cotas;
    }

   
    
    
    
}
